		echo "<center><b>Mnozenie $x * $y wynosi:</b>  ".($x * $y)."<br/></center>";
	}
	
	function dzielenie($x, $y){
		echo "<center><b>Dzielenie $x / $y wynosi:</b>  ".($x / $y)."<br/></center>";
	}
	

}





